Cypress.Commands.add('switchToNewTab', ()=>{
    cy.window().then((win)=>{
        win.open('', '_blank'); //Opens blank tab
    })

    //wait for new tab to open
    cy.window().should('have.length', 2); 

    //switch to the new tab
    cy.window().then((win)=>{
        win.focus()
    })
})


